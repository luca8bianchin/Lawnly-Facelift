/**
 * Filtra catalogo per tipo di prato e stagione corrente
 */
export function filterCatalogForContext(catalog, { grassType, month, audienceLevel = "prosumer" }) {
  const isMicro = grassType === "microterme";
  return catalog.filter(p => {
    // Audience gate
    if (audienceLevel === "retail" && p.audienceLevel === "pro") return false;
    if (audienceLevel === "prosumer" && p.audienceLevel === "pro") return false;
    // Stagione
    const season = isMicro ? p.seasonMicro : p.seasonMacro;
    if (season && season.length > 0 && !season.includes(month)) return false;
    return true;
  });
}

/**
 * Trova prodotti compatibili per un'operazione generata dall'AI
 */
export function matchProductForOperation(operation, catalog, whStock = []) {
  // 1. Match per tipo operazione → tipo prodotto
  const typeMap = {
    "concimazione_azotata": ["concime"],
    "concimazione_potassica": ["concime"],
    "biostimolazione": ["biostimolante"],
    "bioattivazione": ["bioattivato"],
    "wetting_agent": ["surfattante"],
    "correzione_ph": ["correttore"]
  };
  const allowedTypes = typeMap[operation.type] || [];
  let candidates = catalog.filter(p => allowedTypes.includes(p.type));

  // 2. Match per NPK target (se specificato dall'AI)
  if (operation.targetNPK) {
    candidates = candidates.filter(p => {
      if (!p.npk) return false;
      const { n, p: phos, k } = operation.targetNPK;
      if (n != null && Math.abs(p.npk.n - n) > 5) return false;
      if (phos != null && Math.abs(p.npk.p - phos) > 5) return false;
      if (k != null && Math.abs(p.npk.k - k) > 5) return false;
      return true;
    });
  }

  // 3. Priorità ai prodotti in magazzino
  const inStock = candidates.filter(c =>
    whStock.some(w => w.productId === c.id && w.packages?.some(pkg => pkg.remaining > 0))
  );

  return {
    primary: inStock[0] || candidates[0] || null,
    alternatives: (inStock.length > 0 ? inStock : candidates).slice(1, 4),
    fromStock: inStock.length > 0
  };
}

/**
 * Trova prodotto per ID
 */
export function getProductById(catalog, id) {
  return catalog.find(p => p.id === id) || null;
}

/**
 * Trova formato per SKU
 */
export function getFormatBySku(catalog, sku) {
  for (const product of catalog) {
    const fmt = product.formats?.find(f => f.sku === sku);
    if (fmt) return { product, format: fmt };
  }
  return null;
}

